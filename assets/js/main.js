/**
 * News Category Filtering (for news page)
 */
if (document.querySelector('.category-filter')) {
    const filterButtons = document.querySelectorAll('.category-filter');
    const articles = document.querySelectorAll('.news-article');
    
    filterButtons.forEach(button => {
        button.addEventListener('click', function() {
            const category = this.getAttribute('data-category');
            
            // Update active button
            filterButtons.forEach(btn => {
                btn.classList.remove('active');
            });
            this.classList.add('active');
            
            // Filter articles
            articles.forEach(article => {
                if (category === 'all') {
                    article.style.display = 'block';
                    article.style.opacity = '1';
                } else {
                    const articleCategories = article.getAttribute('data-categories');
                    if (articleCategories && articleCategories.includes(category)) {
                        article.style.display = 'block';
                        article.style.opacity = '1';
                    } else {
                        article.style.opacity = '0';
                        setTimeout(() => {
                            if (article.style.opacity === '0') {
                                article.style.display = 'none';
                            }
                        }, 300);
                    }
                }
            });
        });
    });
}

/**
 * Initialize animations on scroll
 */
function initializeScrollAnimations() {
    const animatedElements = document.querySelectorAll('[data-animate]');
    
    if ('IntersectionObserver' in window) {
        const animationObserver = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const element = entry.target;
                    const animation = element.getAttribute('data-animate');
                    element.classList.add('animate-' + animation);
                    animationObserver.unobserve(element);
                }
            });
        }, {
            threshold: 0.1,
            rootMargin: '0px 0px -50px 0px'
        });
        
        animatedElements.forEach(el => animationObserver.observe(el));
    }
}

// Initialize scroll animations
document.addEventListener('DOMContentLoaded', initializeScrollAnimations);

